C FORTRAN I PROGRAM TO COMPUTE PRIME NUMBERS USING TRIAL DIVISION
      DIMENSION IPM(1000)
      NMAX = 1000
      PRINT 9
9     FORMAT(7H PRIMES )

      N = 1
      IPM(1) = 2

1     DO 200 I = 3, NMAX, 2
      IPRM = 1
      LIM = I / 2

C CHECK DIVISIBILITY
2     DO 250 J = 2, LIM
      IF (XMODF(I, J)) 240, 240, 250
240   IPRM = 0
250   CONTINUE

270   IF (IPRM) 200, 200, 280

280   N = N + 1
      IPM(N) = I

200   CONTINUE

      PRINT 900, N
900   FORMAT(6H FOUND , I5, 7H PRIMES)

C PRINT PRIMES
3     DO 500 I = 1, N
      PRINT 910, IPM(I)
910   FORMAT(I5)
500   CONTINUE

      PRINT 920
920   FORMAT(5H DONE)
      STOP

